$( document ).ready()

//=== TECH ===
$(function() {
  $(".top-tech, .dida-tech").hover(function() {
    $(".top-tech").stop().animate({ opacity: 0.15 }, "fast"); //on mouse hover
  },
  function() {
    $(".top-tech").stop().animate({ opacity: 1.0 }, "fast"); //on mouse out
  });
});

$(function() {
  $(".top-tech, .dida-tech").hover(function() {
    $(".dida-tech").stop().animate({ opacity: 1.0 }, "fast"); //on mouse hover
  },
  function() {
    $(".dida-tech").stop().animate({ opacity: 0.0 }, "fast"); //on mouse out
  });
});

